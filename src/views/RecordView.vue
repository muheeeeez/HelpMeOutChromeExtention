<script setup>
import { ref, onMounted } from "vue";
import { useRouter } from "vue-router";
import { useToast } from "vue-toast-notification";
import { useAuthStore } from "@/stores/auth";
import { auth, db } from "../../public/firebase";
import { doc, onSnapshot } from "firebase/firestore";
const authStore = useAuthStore();
const router = useRouter();
const toast = useToast();
const userData = ref({});

const showRecordModal = ref(false);
const videoTitle = ref("Recording");
const recordingState = ref("idle");
console.log(authStore.user);
console.log(userData);
console.log(auth);
function openModal() {
  showRecordModal.value = true;
}

function closeModal() {
  showRecordModal.value = false;
}

function confirmRecording() {
  showRecordModal.value = false;

  const recorderURL =
    chrome.runtime.getURL("recorder.html") +
    "?videoName=" +
    encodeURIComponent(videoTitle.value);
  window.open(recorderURL, "_blank");
}
let unsubscribeUserData = null;
onMounted(() => {
  const user = auth.currentUser;
  if (user) {
    const userDocRef = doc(db, "users", user.uid);
    unsubscribeUserData = onSnapshot(
      userDocRef,
      (docSnap) => {
        if (docSnap.exists()) {
          userData.value = docSnap.data();
        }
      },
      (err) => {
        toast.error("Failed to fetch user data: " + err.message, {
          position: "top-right",
        });
      }
    );
  } else {
    router.push("/login");
  }

  if (
    typeof chrome !== "undefined" &&
    chrome.runtime &&
    chrome.runtime.onMessage
  ) {
    chrome.runtime.onMessage.addListener((message) => {
      console.log("RecordView received message:", message);
      switch (message.action) {
        case "RECORDING_STARTED":
          recordingState.value = "recording";
          break;
        case "UPLOAD_PROGRESS":
          // You can update a progress bar here if desired.
          break;
        case "UPLOAD_COMPLETE":
          recordingState.value = "complete";
          break;
        case "RECORDING_ERROR":
        case "UPLOAD_ERROR":
          recordingState.value = "error";
          break;
      }
    });
  } else {
    console.warn("chrome.runtime.onMessage is not available.");
  }
});
</script>

